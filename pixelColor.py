def generateMapping():
    mapping = [
        ("a", (255, 0, 0)), ("A", (255, 128, 128)),
        ("b", (0, 255, 0)), ("B", (128, 255, 128)),
        ("c", (0, 0, 255)), ("C", (128, 128, 255)),
        ("d", (255, 255, 0)), ("D", (255, 255, 128)),
        ("e", (255, 0, 255)), ("E", (255, 128, 255)),
        ("f", (0, 255, 255)), ("F", (128, 255, 255)),
        ("g", (255, 128, 0)), ("G", (255, 192, 128)),
        ("h", (128, 0, 255)), ("H", (192, 128, 255)),
        ("i", (128, 255, 0)), ("I", (192, 255, 128)),
        ("j", (0, 128, 255)), ("J", (128, 192, 255)),
        ("k", (255, 0, 128)), ("K", (255, 128, 192)),
        ("l", (0, 255, 128)), ("L", (128, 255, 192)),
        ("m", (128, 0, 128)), ("M", (192, 128, 192)),
        ("n", (128, 128, 0)), ("N", (192, 192, 128)),
        ("o", (128, 128, 128)), ("O", (192, 192, 192)),
        ("p", (0, 0, 0)), ("P", (128, 128, 96)),
    ]
    return mapping

def GetRGB(key):
    mapping = generateMapping()
    for k, rgb_value in mapping:
        if key == k:
            return rgb_value
    return None

def GetKey(rgb_value):
    mapping = generateMapping()
    for key, value in mapping:
        if rgb_value == value:
            return key
    return None